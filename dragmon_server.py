LANGCODES = dict(map(reversed, LANGUAGES.items()))
@app.route('/')
def hello_world():
    return 'Hello from Dragmon!'

@app.route('/DetectAndTranslate',methods=['POST'])
def detect_And_translate():
    jsonInputs = request.get_json()
    inputText = jsonInputs['inputText']
    outputLanguage = jsonInputs['outputLanguage']
    translator = Translator()
    detectedLanguage = translator.detect(inputText).lang
    token = sent_tokenize(inputText)
    outputText = ""
    for tt in token:
        translatedText=translator.translate(tt,dest=LANGCODES[outputLanguage])
        outputText = outputText + translatedText.text
    return jsonify({'detectedLanguage':LANGUAGES[detectedLanguage], 'outputText':outputText})

